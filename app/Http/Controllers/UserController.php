    {
        if (!Auth::user()->isAdmin())
            return redirect('/login');

        $users = User::query();
        if (!empty(request('approved'))) {
            $users = $users->where('users.approved', '=', request('approved') == 'true');
        }

        return view('user')->with('users', $users->get());
